TABLE_SCALE = 1.3
MOVE_DY = 36.0
TABLE_ORIGIN_X = 214.0
TABLE_ORIGIN_Y = 201.0


def _tx(x: float) -> float:
    return TABLE_ORIGIN_X + (x - TABLE_ORIGIN_X) * TABLE_SCALE


def _ty(y: float) -> float:
    return TABLE_ORIGIN_Y + MOVE_DY + (y - TABLE_ORIGIN_Y) * TABLE_SCALE


def _ts(v: float) -> float:
    return v * TABLE_SCALE


